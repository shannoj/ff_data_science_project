CREATE DATABASE nfl_stadiums;

USE nfl_stadiums;

CREATE TABLE stadium_locations (
    team VARCHAR(50),
    latitude DECIMAL(9,6),
    longitude DECIMAL(9,6)
);

CREATE TABLE stadium_forecasts (
    team VARCHAR(50),
    temperature INT,
    wind_speed VARCHAR(50),
    forecast VARCHAR(255),
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

